def get_doctor_revenue(
    db: Session, doctor_id: int, start_date: datetime, end_date: datetime
):
    # Calculate total revenue generated by a doctor
    query = db.query(
        func.sum(models.Treatment.cost - models.Treatment.discount).label("revenue"),
        func.count(models.Treatment.id).label("treatment_count"),
    ).filter(
        models.Treatment.doctor_id == doctor_id,
        models.Treatment.date >= start_date,
        models.Treatment.date <= end_date,
        models.Patient.is_deleted == False,
    )
    result = query.first()
    return {
        "revenue": result.revenue or 0.0,
        "treatment_count": result.treatment_count or 0,
    }


def get_treatments(db: Session, patient_id: int, tenant_id: int):
    # 1. Fetch treatments
    treatments = (
        db.query(models.Treatment)
        .join(models.Patient)
        .filter(
            models.Treatment.patient_id == patient_id,
            models.Patient.tenant_id == tenant_id,
            models.Patient.is_deleted == False,
        )
        .all()
    )

    if not treatments:
        return []

    # 2. Reconstruct Consumed Materials from Stock Movements
    # Collect IDs for batch query
    treatment_ids = [str(t.id) for t in treatments]
    reference_ids = [f"TREATMENT:{tid}" for tid in treatment_ids]

    if not reference_ids:
        return treatments

    try:
        from sqlalchemy.orm import joinedload

        # Fetch all movements linked to these treatments
        movements = (
            db.query(models.StockMovement)
            .filter(models.StockMovement.reference_id.in_(reference_ids))
            .join(models.StockItem)
            .join(models.Batch)
            .join(models.Material)
            .options(
                joinedload(models.StockMovement.stock_item)
                .joinedload(models.StockItem.batch)
                .joinedload(models.Batch.material)
            )
            .all()
        )

        # Map movements to treatment_id
        movements_map = {}  # treatment_id -> { material_id: quantity }

        for m in movements:
            try:
                # Parse reference_id "TREATMENT:123"
                parts = m.reference_id.split(":")
                if len(parts) < 2:
                    continue

                t_id = int(parts[1])

                # Use batch material
                mat_id = m.stock_item.batch.material_id
                qty = abs(m.change_amount)

                if t_id not in movements_map:
                    movements_map[t_id] = {}

                # Aggregate quantity for same material
                if mat_id in movements_map[t_id]:
                    movements_map[t_id][mat_id] += qty
                else:
                    movements_map[t_id][mat_id] = qty

            except Exception:
                # print(f"Error parsing movement {m.id}: {e}")
                continue

        # Attach list to treatment objects
        # We need to construct objects compatible with schemas.ConsumedMaterialItem
        # Pydantic will serialize generic dicts/objects if attributes match
        from backend.schemas.clinical import ConsumedMaterialItem

        for t in treatments:
            if t.id in movements_map:
                # Convert dict {mat_id: qty} to list of objects
                items = []
                for mat_id, qty in movements_map[t.id].items():
                    # We can pass simple dicts if Pydantic config allows, otherwise instantiation
                    items.append(ConsumedMaterialItem(material_id=mat_id, quantity=qty))

                # Create a temporary attribute on the ORM object
                # This works because Pydantic `from_attributes=True` reads it
                t.consumedMaterials = items
            else:
                t.consumedMaterials = []

    except Exception as e:
        print(f"Error reconstruction consumed materials: {e}")
        # Return treatments anyway to avoid crashing listing
        pass

    return treatments


