def update_treatment(
    db: Session,
    treatment_id: int,
    treatment: schemas.TreatmentCreate,
    tenant_id: int,
    commit: bool = True,
):
    db_treatment = (
        db.query(models.Treatment)
        .join(models.Patient)
        .filter(
            models.Treatment.id == treatment_id, models.Patient.tenant_id == tenant_id
        )
        .first()
    )
    if db_treatment:
        treatment_dict = treatment.dict()
        # Remove fields that are not part of the Treatment model
        treatment_dict.pop("consumedMaterials", None)
        # Preserve doctor_id if not provided in update
        if not treatment_dict.get("doctor_id") and db_treatment.doctor_id:
            treatment_dict["doctor_id"] = db_treatment.doctor_id
        # Ensure tenant_id is set
        if not treatment_dict.get("tenant_id"):
            treatment_dict["tenant_id"] = tenant_id

        for key, value in treatment_dict.items():
            setattr(db_treatment, key, value)
        for key, value in treatment_dict.items():
            setattr(db_treatment, key, value)

        if commit:
            db.commit()
            db.refresh(db_treatment)
        else:
            db.flush()
            db.refresh(db_treatment)
        return db_treatment


# --- Payment CRUD ---
def create_payment(
    db: Session, payment: schemas.PaymentCreate, tenant_id: int, doctor_id: int = None
):
    """
    Create a payment record.
    If doctor_id is not provided, it will be auto-assigned from the patient's most recent treatment.
    """
    patient = get_patient(db, payment.patient_id, tenant_id)
    if not patient:
        return None

    payment_dict = payment.dict()

    # Auto-assign doctor_id if not provided - get from most recent treatment
    if not payment_dict.get("doctor_id") and not doctor_id:
        # Find most recent treatment for this patient
        recent_treatment = (
            db.query(models.Treatment)
            .filter(
                models.Treatment.patient_id == payment.patient_id,
                models.Treatment.doctor_id.isnot(None),
            )
            .order_by(models.Treatment.date.desc())
            .first()
        )
        if recent_treatment:
            doctor_id = recent_treatment.doctor_id

    # Set doctor_id if available
    if doctor_id and not payment_dict.get("doctor_id"):
        payment_dict["doctor_id"] = doctor_id

    # Ensure tenant_id is set
    if not payment_dict.get("tenant_id"):
        payment_dict["tenant_id"] = tenant_id

    db_payment = models.Payment(**payment_dict)
    db.add(db_payment)
    db.commit()
    db.refresh(db_payment)
    return db_payment


