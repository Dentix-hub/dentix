def get_financial_stats(db: Session, tenant_id: int, doctor_id: int = None):
    """
    Get financial statistics for a tenant.
    """

    today_start = datetime.utcnow().replace(hour=0, minute=0, second=0, microsecond=0)

    # Treatments - all time
    treatments_query = (
        db.query(func.sum(models.Treatment.cost))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id, models.Patient.is_deleted == False
        )
    )

    if doctor_id:
        treatments_query = treatments_query.filter(
            models.Treatment.doctor_id == doctor_id
        )

    total_treatments = treatments_query.scalar() or 0.0
    discounts_query = (
        db.query(func.sum(models.Treatment.discount))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id, models.Patient.is_deleted == False
        )
    )

    if doctor_id:
        discounts_query = discounts_query.filter(
            models.Treatment.doctor_id == doctor_id
        )

    total_discounts = discounts_query.scalar() or 0.0
    total_revenue = float(total_treatments) - float(total_discounts)

    # Treatments - today
    # Treatments - today
    today_revenue_query = (
        db.query(func.sum(models.Treatment.cost - models.Treatment.discount))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id,
            models.Patient.is_deleted == False,
            models.Treatment.date >= today_start,
        )
    )

    if doctor_id:
        today_revenue_query = today_revenue_query.filter(
            models.Treatment.doctor_id == doctor_id
        )

    today_revenue = today_revenue_query.scalar() or 0.0
    today_revenue = float(today_revenue)

    # Payments - all time
    payments_query = (
        db.query(func.sum(models.Payment.amount))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id, models.Patient.is_deleted == False
        )
    )

    if doctor_id:
        payments_query = payments_query.filter(models.Payment.doctor_id == doctor_id)

    total_received = payments_query.scalar() or 0.0
    total_received = float(total_received)

    # Payments - today
    # Payments - today
    today_received_query = (
        db.query(func.sum(models.Payment.amount))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id,
            models.Patient.is_deleted == False,
            models.Payment.date >= today_start,
        )
    )

    if doctor_id:
        today_received_query = today_received_query.filter(
            models.Payment.doctor_id == doctor_id
        )

    today_received = today_received_query.scalar() or 0.0
    today_received = float(today_received)

    outstanding = total_revenue - total_received
    today_outstanding = max(0, today_revenue - today_received)

    # Lab costs - all time
    lab_costs_query = (
        db.query(func.sum(models.LabOrder.cost))
        .join(models.Patient)
        .filter(
            models.LabOrder.tenant_id == tenant_id, models.Patient.is_deleted == False
        )
    )

    if doctor_id:
        lab_costs_query = lab_costs_query.filter(models.LabOrder.doctor_id == doctor_id)

    total_lab_costs = lab_costs_query.scalar() or 0.0
    total_lab_costs = float(total_lab_costs)

    # Lab costs - today
    # Lab costs - today
    today_lab_costs_query = (
        db.query(func.sum(models.LabOrder.cost))
        .join(models.Patient)
        .filter(
            models.LabOrder.tenant_id == tenant_id,
            models.LabOrder.order_date >= today_start,
            models.Patient.is_deleted == False,
        )
    )

    if doctor_id:
        today_lab_costs_query = today_lab_costs_query.filter(
            models.LabOrder.doctor_id == doctor_id
        )

    today_lab_costs = today_lab_costs_query.scalar() or 0.0
    today_lab_costs = float(today_lab_costs)

    # Expenses - all time
    if doctor_id:
        total_expenses = 0.0
    else:
        total_expenses = (
            db.query(func.sum(models.Expense.cost))
            .filter(models.Expense.tenant_id == tenant_id)
            .scalar()
            or 0.0
        )
    total_expenses = float(total_expenses)

    # Expenses - today
    if doctor_id:
        today_expenses = 0.0
    else:
        today_expenses = (
            db.query(func.sum(models.Expense.cost))
            .filter(
                models.Expense.tenant_id == tenant_id,
                models.Expense.date == date.today(),
            )
            .scalar()
            or 0.0
        )
    today_expenses = float(today_expenses)

    # Net Profit = Received - Expenses - Lab Costs
    net_profit = total_received - total_expenses - total_lab_costs

    return {
        "total_revenue": total_revenue,
        "total_received": total_received,
        "outstanding": outstanding if outstanding > 0 else 0,
        "total_expenses": total_expenses + total_lab_costs,
        "net_profit": net_profit,
        "monthly_revenue": 0.0,
        "today_revenue": today_revenue,
        "today_received": today_received,
        "today_outstanding": today_outstanding,
        "today_expenses": today_expenses + today_lab_costs,
    }


def get_dashboard_stats(db: Session, tenant_id: int, doctor_id: int = None):
    # Reuse financial stats logic
    fin_stats = get_financial_stats(db, tenant_id, doctor_id)

    # 1. Total Patients Count (O(1) with optimized count)
    # We can't easily get current_user here, but if doctor_id is passed, we can filter.
    # However, patients filtering is complex (via visibility service).
    # For now, if doctor_id passed, simpler filtering "Allocated to this doctor"?
    # Or count only Assigned?

    patients_query = db.query(func.count(models.Patient.id)).filter(
        models.Patient.tenant_id == tenant_id, models.Patient.is_deleted == False
    )
    if doctor_id:
        # Strict mode: Only assigned patients
        patients_query = patients_query.filter(
            models.Patient.assigned_doctor_id == doctor_id
        )

    total_patients = patients_query.scalar() or 0

    # 2. Today's Appointments Count
    today_start = datetime.utcnow().replace(hour=0, minute=0, second=0, microsecond=0)

    # 2. New Patients Today
    new_patients_query = db.query(func.count(models.Patient.id)).filter(
        models.Patient.tenant_id == tenant_id,
        models.Patient.is_deleted == False,
        func.date(models.Patient.created_at) == func.date(today_start),
    )

    if doctor_id:
        new_patients_query = new_patients_query.filter(
            models.Patient.assigned_doctor_id == doctor_id
        )

    new_patients_today = new_patients_query.scalar() or 0

    # 3. Today's Appointments Count
    appointments_query = (
        db.query(func.count(models.Appointment.id))
        .join(models.Patient)
        .filter(
            models.Patient.tenant_id == tenant_id,
            models.Patient.is_deleted == False,
            models.Appointment.is_deleted == False,
            models.Appointment.status != "Cancelled",  # FIX: Exclude cancelled
            func.date(models.Appointment.date_time) == func.date(today_start),
        )
    )

    if doctor_id:
        appointments_query = appointments_query.filter(
            models.Appointment.doctor_id == doctor_id
        )

    total_appointments_today = appointments_query.scalar() or 0

    # 4. Revenue Chart (Last 7 Days)
    from datetime import timedelta

    chart_data = []
    for i in range(6, -1, -1):
        day = today_start - timedelta(days=i)
        next_day = day + timedelta(days=1)

        daily_rev_query = (
            db.query(func.sum(models.Payment.amount))
            .join(models.Patient)
            .filter(
                models.Patient.tenant_id == tenant_id,
                models.Patient.is_deleted == False,
                models.Payment.date >= day,
                models.Payment.date < next_day,
            )
        )

        if doctor_id:
            daily_rev_query = daily_rev_query.filter(
                models.Payment.doctor_id == doctor_id
            )

        daily_rev = daily_rev_query.scalar() or 0.0
        chart_data.append(
            {"name": day.strftime("%Y-%m-%d"), "revenue": float(daily_rev)}
        )

    return {
        **fin_stats,
        "total_patients": total_patients,
        "new_patients_today": new_patients_today,
        "total_appointments_today": total_appointments_today,
        "revenue_chart": chart_data,
    }
